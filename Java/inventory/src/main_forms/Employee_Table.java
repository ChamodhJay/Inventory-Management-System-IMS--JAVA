    private void tableDataClear(){
        try {
            while (0<=empTbl.getRowCount()){
                DefaultTableModel table = (DefaultTableModel)empTbl.getModel();
                table.removeRow(empTbl.getRowCount()-1);
            }
        } catch (Exception e) {
            System.out.println("tableDataClear: "+e);
        }
    }
    
    private void tableDataLoader (){
        try {
            java.sql.ResultSet rs = DB.getdata("SELECT * FROM empolyee");   
            while (rs.next()){
                String ID = String.valueOf(rs.getString("Emp_ID"));
                String Name = String.valueOf(rs.getString("Emp_Name"));
                String Address = String.valueOf(rs.getString("Address"));
                String NIC = String.valueOf(rs.getString("NIC"));
                String TP = String.valueOf(rs.getString("Contact_Number"));
                String Des = String.valueOf(rs.getString("Discription"));
                
                
                
                String table_data[] = {ID,Name,Address,NIC,TP,Des};
                DefaultTableModel table = (DefaultTableModel)empTbl.getModel();
                
                table.addRow(table_data);
            
        }
            
        } catch (Exception e) {
            System.out.println("Error(tableDataLoader): "+e);
        }
    }

